// Convert 1-bit-per-pixel bitmap to RGBA texture data
static std::vector<uint8_t> buildFontAtlasRGBA() {
    const int totalPixels = FontRenderer::ATLAS_W * FontRenderer::ATLAS_H;
    std::vector<uint8_t> rgba(totalPixels * 4, 0);

    for (int charIdx = 0; charIdx < FontRenderer::GLYPH_COUNT; ++charIdx) {
        for (int row = 0; row < FontRenderer::GLYPH_H; ++row) {
            uint8_t byte = FONT_BITMAP[charIdx * FontRenderer::GLYPH_H + row];
            for (int col = 0; col < FontRenderer::GLYPH_W; ++col) {
                int atlasX = charIdx * FontRenderer::GLYPH_W + col;
                int atlasY = row;
                int pixelIdx = atlasY * FontRenderer::ATLAS_W + atlasX;
                int rgbaIdx = pixelIdx * 4;

                // MSB = leftmost pixel
                bool lit = (byte >> (7 - col)) & 1;
                if (lit) {
                    rgba[rgbaIdx + 0] = 255;
                    rgba[rgbaIdx + 1] = 255;
                    rgba[rgbaIdx + 2] = 255;
                    rgba[rgbaIdx + 3] = 255;
                } else {
                    rgba[rgbaIdx + 0] = 0;
                    rgba[rgbaIdx + 1] = 0;
                    rgba[rgbaIdx + 2] = 0;
                    rgba[rgbaIdx + 3] = 0;
                }
            }
        }
    }
    return rgba;
}

// ── Constructor / Destructor ──────────────────────────────────────────────

FontRenderer::FontRenderer() = default;

FontRenderer::~FontRenderer() {
    if (m_vboUV) GL_CHECK(glDeleteBuffers(1, &m_vboUV));
    if (m_vboPos) GL_CHECK(glDeleteBuffers(1, &m_vboPos));
    if (m_vao)  GL_CHECK(glDeleteVertexArrays(1, &m_vao));
    if (m_atlasTexture) GL_CHECK(glDeleteTextures(1, &m_atlasTexture));
}

// ── Initialization ────────────────────────────────────────────────────────

void FontRenderer::initialize() {
    // Compile text shader
    m_shader = std::make_unique<Shader>(
        "assets/shaders/text.vert",
        "assets/shaders/text.frag"
    );

    // Create font atlas texture as RGBA (since GL_R8 may not be available)
    auto atlasData = buildFontAtlasRGBA();

    GL_CHECK(glGenTextures(1, &m_atlasTexture));
    GL_CHECK(glBindTexture(GL_TEXTURE_2D, m_atlasTexture));
    GL_CHECK(glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA,
                 ATLAS_W, ATLAS_H, 0,
                 GL_RGBA, GL_UNSIGNED_BYTE, atlasData.data()));
    GL_CHECK(glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST));
    GL_CHECK(glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST));
    GL_CHECK(glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE));
    GL_CHECK(glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE));

    // Create VAO with dynamic position and UV buffers (no EBO — use glDrawArrays)
    GL_CHECK(glGenVertexArrays(1, &m_vao));
    GL_CHECK(glBindVertexArray(m_vao));

    // Position buffer (dynamic)
    GL_CHECK(glGenBuffers(1, &m_vboPos));
    GL_CHECK(glBindBuffer(GL_ARRAY_BUFFER, m_vboPos));
    GL_CHECK(glVertexAttribPointer(0, 2, GL_FLOAT, GL_FALSE, 0, nullptr));
    GL_CHECK(glEnableVertexAttribArray(0));

    // UV buffer (dynamic)
    GL_CHECK(glGenBuffers(1, &m_vboUV));
    GL_CHECK(glBindBuffer(GL_ARRAY_BUFFER, m_vboUV));
    GL_CHECK(glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 0, nullptr));
    GL_CHECK(glEnableVertexAttribArray(1));

    GL_CHECK(glBindVertexArray(0));
}

// ── Begin / End ───────────────────────────────────────────────────────────

void FontRenderer::begin(const glm::mat4& projection) {
    m_shader->bind();
    m_shader->setMat4("uProjection", projection);
    GL_CHECK(glActiveTexture(GL_TEXTURE0));
    GL_CHECK(glBindTexture(GL_TEXTURE_2D, m_atlasTexture));
    m_shader->setInt("uFontAtlas", 0);
}

void FontRenderer::end() {
    m_shader->unbind();
}

// ── Text measurement ──────────────────────────────────────────────────────

glm::vec2 FontRenderer::measureText(const std::string& text, float scale) const {
    float maxWidth = 0.0f;
    float lineWidth = 0.0f;
    int lines = 1;

    for (char c : text) {
        if (c == '\n') {
            maxWidth = std::max(maxWidth, lineWidth);
            lineWidth = 0.0f;
            ++lines;
            continue;
        }
        if (c == ' ' || c == 'i' || c == 'l' || c == 'I' || c == '|' || c == ':') {
            lineWidth += GLYPH_W * scale * 0.5f;
        } else if (c == 't' || c == 'f' || c == 'j' || c == 'r' || c == '[' || c == ']') {
            lineWidth += GLYPH_W * scale * 0.7f;
        } else {
            lineWidth += GLYPH_W * scale * 0.85f;
        }
    }
    maxWidth = std::max(maxWidth, lineWidth);
    return glm::vec2(maxWidth, GLYPH_H * scale * static_cast<float>(lines));
}

// ── Text rendering ────────────────────────────────────────────────────────

void FontRenderer::renderText(const std::string& text, float x, float y,
                               float scale, const glm::vec3& color) {
    if (text.empty()) return;

    m_shader->setVec3("uTextColor", color);

    // 6 vertices per character (2 triangles, no index buffer)
    std::vector<float> positions;
    std::vector<float> uvs;
    positions.reserve(text.size() * 12);  // 6 verts × 2 floats
    uvs.reserve(text.size() * 12);

    float cursorX = x;
    float cursorY = y;

    for (char c : text) {
        if (c == '\n') {
            cursorX = x;
            cursorY -= GLYPH_H * scale;
            continue;
        }

        int charCode = static_cast<unsigned char>(c);
        if (charCode < FIRST_GLYPH || charCode >= FIRST_GLYPH + GLYPH_COUNT) {
            cursorX += GLYPH_W * scale * 0.5f;
            continue;
        }

        int charIndex = charCode - FIRST_GLYPH;

        float u0 = static_cast<float>(charIndex * GLYPH_W) / ATLAS_W;
        float u1 = static_cast<float>((charIndex + 1) * GLYPH_W) / ATLAS_W;
        float v0 = 0.0f;  // bottom of glyph in texture atlas
        float v1 = 1.0f;  // top of glyph in texture atlas

        float x0 = cursorX;
        float x1 = cursorX + GLYPH_W * scale;
        float y0 = cursorY;
        float y1 = cursorY + GLYPH_H * scale;

        // 2 triangles = 6 vertices (CCW winding)
        // Triangle 1: bottom-left, bottom-right, top-right
        // Triangle 2: bottom-left, top-right, top-left
        float quadPos[12] = {
            x0,y0,  x1,y0,  x1,y1,   // tri 1
            x0,y0,  x1,y1,  x0,y1    // tri 2
        };
        float quadUV[12] = {
            u0,v1,  u1,v1,  u1,v0,   // tri 1
            u0,v1,  u1,v0,  u0,v0    // tri 2
        };

        positions.insert(positions.end(), std::begin(quadPos), std::end(quadPos));
        uvs.insert(uvs.end(), std::begin(quadUV), std::end(quadUV));

        // Advance cursor (proportional spacing)
        if (c == ' ' || c == 'i' || c == 'l' || c == 'I' || c == '|' || c == ':') {
            cursorX += GLYPH_W * scale * 0.5f;
        } else if (c == 't' || c == 'f' || c == 'j' || c == 'r' || c == '[' || c == ']') {
            cursorX += GLYPH_W * scale * 0.7f;
        } else {
            cursorX += GLYPH_W * scale * 0.85f;
        }
    }

    if (positions.empty()) return;

    GL_CHECK(glBindVertexArray(m_vao));

    GL_CHECK(glBindBuffer(GL_ARRAY_BUFFER, m_vboPos));
    GL_CHECK(glBufferData(GL_ARRAY_BUFFER, positions.size() * sizeof(float),
                 positions.data(), GL_DYNAMIC_DRAW));

    GL_CHECK(glBindBuffer(GL_ARRAY_BUFFER, m_vboUV));
    GL_CHECK(glBufferData(GL_ARRAY_BUFFER, uvs.size() * sizeof(float),
                 uvs.data(), GL_DYNAMIC_DRAW));

    // Draw all characters in one call (6 vertices per char = 2 triangles)
    int vertCount = static_cast<int>(positions.size() / 2);  // 2 floats per vertex
    GL_CHECK(glDrawArrays(GL_TRIANGLES, 0, vertCount));

    GL_CHECK(glBindVertexArray(0));
}
